export function getGameFeatures(attributes: {
  [key: (typeof customAttributes)[number]]: {
    type: string;
    value: string;
  };
}) {
  const features = Object.keys(attributes).filter((key) =>
    gameFeatures.includes(key),
  );
  return features;
}
